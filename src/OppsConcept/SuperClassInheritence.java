public class SuperClassInheritence 
{

	public static void method()
	{
		System.out.println("Rameshwar Rathod");
	}
	
	public static void method(int a)
	{
		System.out.println("Nitin Satpute");
	}
	public static void method(char a)
	{
		System.out.println("Snehal Patil");
	}
    public void method1()
     {
	 System.out.println("Sneha Shambhakar");
      }
    public void method1(int a)
    {
    	System.out.println("Bhagvat Dhade");
    }
    public void method1(char b)
    {
    	System.out.println("Varsha Patil");
    }
}
