package OppsConcept;

public class MainMethodOverLoadSuperClass 
{
public static void main(String [] args)
{
	System.out.println("Nitin Satpute");
}

public static void main(int [] args)
{
	System.out.println("Snehal Patil");
}

public static void main(char [] args)
{
	System.out.println("Sneha Shambhakar");
}

public static void main(short [] args)
{
	System.out.println("Varsha Patil");
}


}
